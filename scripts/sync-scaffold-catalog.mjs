export const INTRO_CLAUDE_WORKFLOWS = `**Qué son.** Cada archivo \`*.js\` de este directorio es un **script de orquestación** que ejecuta la **Workflow tool**. Un script es JavaScript plano que llama a unos pocos **helper-globals** inyectados (\`agent\`, \`parallel\`, \`pipeline\`, \`workflow\`, \`phase\`, \`log\`, más \`args\`) para abrir subagentes, iterar, verificar y componer; no hay \`import\`, ni \`require\`, ni \`ctx.*\`. Le pasás un objeto JSON \`args\`; el script devuelve un valor. Este catálogo tiene **25** workflows.

`;

export function parseCheckOnly(args = process.argv.slice(2)) {
	return args.includes("--check");
}

export function catalogPaths(repo = REPO) {
	const ref = join(repo, ".pi", "skills", "ultracode", "reference");
	return {
		canonical: join(ref, "catalog-prose.es.md"),
		claudeWorkflowsReadme: join(repo, ".claude", "workflows", "README.md"),
		scaffoldCatalog: join(ref, "scaffold-catalog.md"),
		claudeWorkflowsSkillReadme: join(ref, "claude-workflows", "README.md"),
	};
}

/** Extrae el cuerpo editable (sin el comentario de fuente canónica al inicio). */
export function extractCanonicalBody(raw) {
	const withoutBanner = raw.replace(/^<!--[\s\S]*?-->\s*/u, "");
	return withoutBanner.trimStart();
}

export function buildClaudeWorkflowsReadme(body) {
	return GENERATED_MARKER + TITLE + INTRO_SCAFFOLD + body;
}

export function buildScaffoldCatalog(body) {
	return GENERATED_MARKER + TITLE + INTRO_SCAFFOLD + body;
}

export function buildClaudeWorkflowsSkillReadme(body) {
	return GENERATED_MARKER + TITLE + PROVENANCE_BLOCK + INTRO_CLAUDE_WORKFLOWS + body;
}

export function expectedOutputs(body) {
	return {
		claudeWorkflowsReadme: buildClaudeWorkflowsReadme(body),
		scaffoldCatalog: buildScaffoldCatalog(body),
		claudeWorkflowsSkillReadme: buildClaudeWorkflowsSkillReadme(body),
	};
}

export async function syncScaffoldCatalog({
	checkOnly = false,
	paths = catalogPaths(),
	log = console.log,
	error = console.error,
} = {}) {
	const raw = await readMaybe(paths.canonical);
	if (raw === null) {
		error("[sync-scaffold-catalog] ✗ missing canonical: reference/catalog-prose.es.md");
		return { ok: false, wrote: false, drift: false, missing: true };
	}

	const body = extractCanonicalBody(raw);
	const expected = expectedOutputs(body);
	const destinations = [
		{ label: ".claude/workflows/README.md", path: paths.claudeWorkflowsReadme, want: expected.claudeWorkflowsReadme },
		{ label: "reference/scaffold-catalog.md", path: paths.scaffoldCatalog, want: expected.scaffoldCatalog },
		{
			label: "reference/claude-workflows/README.md",
			path: paths.claudeWorkflowsSkillReadme,
			want: expected.claudeWorkflowsSkillReadme,
		},
	];

	let drift = 0;
	let wrote = 0;
	for (const { label, path, want } of destinations) {
		const have = await readMaybe(path);
		if (have === want) continue;
		if (checkOnly) {
			error(`[sync-scaffold-catalog] ✗ drift: ${label}`);
			drift++;
			continue;
		}
		await writeFile(path, want);
		log(`[sync-scaffold-catalog] wrote ${label}`);
		wrote++;
	}

	if (checkOnly) {
		if (drift > 0) {
			error("[sync-scaffold-catalog] ✗ drift — run: npm run sync:scaffold-catalog");
			return { ok: false, wrote: false, drift: true, missing: false, driftCount: drift };
		}
		log("[sync-scaffold-catalog] ✅ catalog prose is in sync.");
		return { ok: true, wrote: false, drift: false, missing: false, driftCount: 0 };
	}

	if (wrote === 0) log("[sync-scaffold-catalog] ✅ catalog prose is in sync.");
	else log("[sync-scaffold-catalog] ✅ wrote catalog destinations from canonical prose.");
	return { ok: true, wrote: wrote > 0, drift: false, missing: false };
}

async function main(args = process.argv.slice(2)) {
	const result = await syncScaffoldCatalog({ checkOnly: parseCheckOnly(args) });
	if (!result.ok) process.exit(1);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) await main();
