#!/usr/bin/env node
/**
 * Codegen: inline the dynamic-workflow scaffold sources into a committed TS map.
 *
 * Source of truth: extensions/pi-dynamic-workflows/scaffolds/*.js (real, lintable,
 * node --check-able workflow files). This script reads each file's exact source
 * text and emits extensions/pi-dynamic-workflows/scaffolds.generated.ts, whose
 * EMBEDDED_SCAFFOLD_SOURCES map pattern-scaffolds.ts imports at runtime — so the scaffold
 * code ships INSIDE the package as data (no runtime filesystem dependency) while
 * being authored as ordinary files.
 *
 *   node scripts/gen-scaffolds.mjs            # regenerate the committed .ts
 *   node scripts/gen-scaffolds.mjs --check    # fail (exit 1) if it is out of sync
 */
import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
export const SCAFFOLDS_DIR = path.join(REPO_ROOT, "extensions", "pi-dynamic-workflows", "scaffolds");
export const OUT_FILE = path.join(REPO_ROOT, "extensions", "pi-dynamic-workflows", "scaffolds.generated.ts");

export function readSources() {
	const files = fs
		.readdirSync(SCAFFOLDS_DIR)
		.filter((f) => f.endsWith(".js"))
		.sort();
	if (files.length === 0) throw new Error(`no scaffolds found in ${SCAFFOLDS_DIR}`);
	const map = {};
	for (const file of files) {
		const key = file.replace(/\.js$/, "");
		map[key] = fs.readFileSync(path.join(SCAFFOLDS_DIR, file), "utf8");
	}
	return map;
}

export function render(map) {
	const entries = Object.keys(map)
		.sort()
		.map((key) => `\t${JSON.stringify(key)}: ${JSON.stringify(map[key])},`)
		.join("\n");
	return (
		"// AUTO-GENERATED by scripts/gen-scaffolds.mjs — DO NOT EDIT BY HAND.\n" +
		"// Source of truth: extensions/pi-dynamic-workflows/scaffolds/*.js\n" +
		"// Regenerate with: npm run generate\n" +
		"\n" +
		"export const EMBEDDED_SCAFFOLD_SOURCES: Record<string, string> = {\n" +
		`${entries}\n` +
		"};\n"
	);
}

// Run the CLI only when invoked directly (so tests can import the helpers above).
if (import.meta.url === pathToFileURL(process.argv[1] ?? "").href) {
	const generated = render(readSources());
	if (process.argv.includes("--check")) {
		const current = fs.existsSync(OUT_FILE) ? fs.readFileSync(OUT_FILE, "utf8") : "";
		if (current !== generated) {
			console.error(
				"scaffolds.generated.ts is OUT OF SYNC with scaffolds/*.js.\n" +
					"Run `npm run generate` and commit the result.",
			);
			process.exit(1);
		}
		console.log("scaffolds.generated.ts is in sync.");
	} else {
		fs.writeFileSync(OUT_FILE, generated);
		console.log(`wrote ${path.relative(REPO_ROOT, OUT_FILE)} (${Object.keys(readSources()).length} scaffolds)`);
	}
}
