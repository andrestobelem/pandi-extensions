#!/usr/bin/env node
// sync-personas-readme.mjs — espeja las personas del proyecto (.pi/personas/*.json) en
// .pi/personas/README.md + README.html COMMITTEADOS y legibles por humanos (con estilo pandi vía el
// converter de pandi-docs). Tiene la misma forma generator + --check que los otros scripts sync-*.mjs:
// ambos mirrors son artifacts GENERATED — no los edites a mano; editá el JSON (la fuente de verdad,
// inyectada al spawn) y re-ejecutá esto.
//
// Los prompts se renderizan legibles pero VERBATIM: las checklists "(N)" pasan a listas ordenadas
// y la prosa pasa a párrafos por oración; si se quita el andamiaje Markdown se reconstruye exactamente
// el string fuente (pineado por el test de roundtrip sin pérdida).
//
// Uso:
//   node scripts/sync-personas-readme.mjs           # escribe/refresca los mirrors
//   node scripts/sync-personas-readme.mjs --check   # solo verifica; sale con 1 si hay drift (sin writes)

import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const REPO = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const CONVERTER = path.join(REPO, "extensions", "pandi-docs", "scripts", "markdown-to-html.mjs");
const { renderMarkdownToHtml } = await import(pathToFileURL(CONVERTER).href);

// Los strings largos de prompt reciben sus propias secciones legibles en vez de entradas meta de una línea.
const PROMPT_KEYS = ["systemPrompt", "appendSystemPrompt"];

// Separador de oraciones para la prosa de prompts: corta en ". " antes de una mayúscula (o "("), pero nunca
// después de abreviaturas comunes ni sobre "?"/"!" (las rachas de preguntas quedan en un solo párrafo).
const ABBREV_TAIL = /\b(?:e\.g|i\.e|vs|etc|cf)\.$/;
function splitSentences(text) {
	const out = [];
	let start = 0;
	const re = /\. (?=[A-Z(])/g;
	let m = re.exec(text);
	for (; m !== null; m = re.exec(text)) {
		const candidate = text.slice(start, m.index + 1);
		if (ABBREV_TAIL.test(candidate.trimEnd())) continue;
		out.push(candidate.trim());
		start = m.index + 2;
	}
	const tail = text.slice(start).trim();
	if (tail) out.push(tail);
	return out;
}

// "Fear check — name it." -> "**Fear check** — name it." (solo presentación).
const boldLead = (item) => item.replace(/^(.{2,80}?) — /, "**$1** — ");

// Renderiza un string de prompt como Markdown legible-pero-verbatim: las checklists "(1) … (2) …"
// se vuelven listas ordenadas (la prosa sobrante después de un item vuelve a párrafos); todo lo
// demás se vuelve párrafos por oración. Los markers deben ascender desde 1 para contar como checklist,
// así que las citas sueltas como "(3)" quedan intactas.
export function renderPromptMarkdown(text) {
	const source = String(text).trim();
	const parts = source.split(/\s*\((\d+)\)\s+/);
	const numbers = [];
	for (let i = 1; i < parts.length; i += 2) numbers.push(Number(parts[i]));
	const isChecklist = numbers.length > 0 && numbers.every((n, i) => n === i + 1);

	const blocks = [];
	const pushProse = (chunk) => {
		for (const s of splitSentences(chunk)) blocks.push({ type: "p", text: s });
	};
	if (!isChecklist) pushProse(source);
	else {
		if (parts[0].trim()) pushProse(parts[0].trim());
		for (let i = 1; i < parts.length; i += 2) {
			const sentences = splitSentences(parts[i + 1].trim());
			blocks.push({ type: "li", n: Number(parts[i]), text: sentences[0] ?? "" });
			for (const s of sentences.slice(1)) blocks.push({ type: "p", text: s });
		}
	}

	let md = "";
	for (let i = 0; i < blocks.length; i++) {
		const b = blocks[i];
		const rendered = b.type === "li" ? `${b.n}. ${boldLead(b.text)}` : b.text;
		if (i === 0) md = rendered;
		else md += (blocks[i - 1].type === "li" && b.type === "li" ? "\n" : "\n\n") + rendered;
	}
	return md;
}

// Render puro: [{ name, data }] (ya ordenado) -> el README Markdown completo.
export function renderPersonasReadme(personas) {
	const lines = [
		"<!-- GENERATED by scripts/sync-personas-readme.mjs — do NOT hand-edit. -->",
		"<!-- Source of truth: the sibling *.json files. Re-run `npm run sync:personas`. -->",
		"",
		"# Project personas",
		"",
		'Read-only advisor personas for subagents, selected with `agentType: "<name>"` and loaded by',
		"`extensions/pandi-dynamic-workflows/agent-env-persona.ts` (project personas override built-ins;",
		"explicit call options win). The JSON files are the source of truth and are injected at spawn;",
		"this README is their readable mirror.",
		"",
	];
	if (!personas.length) lines.push("_No personas defined._", "");
	else {
		lines.push("| Persona | thinking | skills | prompts |", "| --- | --- | --- | --- |");
		for (const { name, data } of personas) {
			const skills = Array.isArray(data.skills) && data.skills.length ? data.skills.join(", ") : "—";
			const chars = PROMPT_KEYS.map((k) => (typeof data[k] === "string" ? data[k].length : 0));
			lines.push(`| [${name}](#${name}) | ${data.thinking ?? "—"} | ${skills} | ${chars[0]} + ${chars[1]} chars |`);
		}
		lines.push("");
	}
	for (let i = 0; i < personas.length; i++) {
		const { name, data } = personas[i];
		if (i > 0) lines.push("---", "");
		lines.push(`## ${name}`, "");
		for (const [key, value] of Object.entries(data)) {
			if (PROMPT_KEYS.includes(key)) continue;
			lines.push(`- **${key}**: \`${JSON.stringify(value)}\``);
		}
		for (const key of PROMPT_KEYS) {
			if (typeof data[key] !== "string") continue;
			lines.push("", `### ${key} (${data[key].length} chars)`, "", renderPromptMarkdown(data[key]));
		}
		lines.push("");
	}
	return lines.join("\n");
}

// Carga .pi/personas/*.json como [{ name, data }], ordenado por nombre de archivo para mantener determinismo.
export function loadPersonas(dir) {
	if (!fs.existsSync(dir)) return [];
	return fs
		.readdirSync(dir)
		.filter((f) => f.endsWith(".json"))
		.sort()
		.map((f) => ({
			name: f.slice(0, -".json".length),
			data: JSON.parse(fs.readFileSync(path.join(dir, f), "utf8")),
		}));
}

// Sincroniza README.md + README.html bajo <root>/.pi/personas/. check:true nunca escribe.
export function syncPersonasReadme(root, { check = false } = {}) {
	const dir = path.join(root, ".pi", "personas");
	const md = renderPersonasReadme(loadPersonas(dir));
	// El header de pandi quiere el H1 como primera línea no vacía; los comentarios GENERATED
	// solo importan en la fuente Markdown, así que se eliminan antes de convertir.
	const html = renderMarkdownToHtml(md.replace(/^(?:<!--[^\n]*-->\n)+/, ""), { kicker: ".pi/personas" });
	let changed = false;
	for (const [file, content] of [
		[path.join(dir, "README.md"), md],
		[path.join(dir, "README.html"), html],
	]) {
		const have = fs.existsSync(file) ? fs.readFileSync(file, "utf8") : null;
		if (have === content) continue;
		changed = true;
		if (!check) {
			fs.mkdirSync(dir, { recursive: true });
			fs.writeFileSync(file, content);
		}
	}
	return { changed };
}

const isMain = process.argv[1] && pathToFileURL(path.resolve(process.argv[1])).href === import.meta.url;
if (isMain) {
	const check = process.argv.includes("--check");
	const { changed } = syncPersonasReadme(REPO, { check });
	if (check) {
		if (changed) {
			console.error("[sync-personas-readme] ✗ .pi/personas/README.md drifted from the JSON sources");
			console.error("[sync-personas-readme]   run `npm run sync:personas` and commit the result");
			process.exit(1);
		}
		console.log("[sync-personas-readme] ✓ .pi/personas/README.md is in sync");
	} else {
		console.log(
			changed
				? "[sync-personas-readme] wrote .pi/personas/README.md + README.html"
				: "[sync-personas-readme] already in sync",
		);
	}
}
